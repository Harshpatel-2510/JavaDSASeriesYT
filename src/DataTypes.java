public class DataTypes {
    public static void main(){

//    numeric DT- short, byte, int, long
        byte num = 127;
        System.out.println(num);

        short nuu = 2500;
        System.out.println(nuu);

        int num2 = 512354;
        System.out.println(num2);

        long num3 = 216102;
        System.out.println(num3);



//      double / float DataTypes
        float num4 = 3.10f;
        System.out.println(num4);

        double num5 = 3.51588285;
        System.out.println(num5);



//      Other DataType = char, boolean
        boolean eligibleToVote = true;
        System.out.println(eligibleToVote);

        char firstclass = 'H';
        System.out.println("My first character is " + (char)(firstclass+10));

    }
}
